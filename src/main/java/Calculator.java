
class Calculator {
    public Calculator() {}

    public int plus(int a, int b) {
        return a + b;
    }

    public int minus(int a, int b) {
        return a - b;
    }

    public int ymn(int a, int b) {
        return a * b;
    }

    public int delen(int a, int b) {
        return a / b;
    }
}